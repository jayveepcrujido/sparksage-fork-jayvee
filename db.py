from __future__ import annotations

import os
import json
import aiosqlite

DATABASE_PATH = os.getenv("DATABASE_PATH", "sparksage.db")

_db: aiosqlite.Connection | None = None


async def get_db() -> aiosqlite.Connection:
    """Return the shared database connection, creating it if needed."""
    global _db
    if _db is None:
        _db = await aiosqlite.connect(DATABASE_PATH)
        _db.row_factory = aiosqlite.Row
        await _db.execute("PRAGMA journal_mode=WAL")
        await _db.execute("PRAGMA foreign_keys=ON")
    return _db


async def init_db():
    """Create tables if they don't exist and run migrations."""
    db = await get_db()
    # base schema without FTS or guild_id-dependent triggers
    await db.executescript(
        """
        CREATE TABLE IF NOT EXISTS config (
            key   TEXT PRIMARY KEY,
            value TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS conversations (
            id         INTEGER PRIMARY KEY AUTOINCREMENT,
            channel_id TEXT    NOT NULL,
            role       TEXT    NOT NULL,
            content    TEXT    NOT NULL,
            provider   TEXT,
            category   TEXT,
            created_at TEXT    NOT NULL DEFAULT (datetime('now'))
        );
        CREATE INDEX IF NOT EXISTS idx_conv_channel ON conversations(channel_id);

        CREATE TABLE IF NOT EXISTS sessions (
            token      TEXT PRIMARY KEY,
            user_id    TEXT NOT NULL,
            created_at TEXT NOT NULL DEFAULT (datetime('now')),
            expires_at TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS wizard_state (
            id           INTEGER PRIMARY KEY CHECK (id = 1),
            completed    INTEGER NOT NULL DEFAULT 0,
            current_step INTEGER NOT NULL DEFAULT 0,
            data         TEXT    NOT NULL DEFAULT '{}'
        );

        CREATE TABLE IF NOT EXISTS faqs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            guild_id TEXT NOT NULL,
            question TEXT NOT NULL,
            answer TEXT NOT NULL,
            match_keywords TEXT NOT NULL,
            times_used INTEGER DEFAULT 0,
            created_by TEXT,
            created_at TEXT DEFAULT (datetime('now'))
        );

        CREATE TABLE IF NOT EXISTS command_permissions (
            command_name TEXT NOT NULL,
            guild_id TEXT NOT NULL,
            role_id TEXT NOT NULL,
            PRIMARY KEY (command_name, guild_id, role_id)
        );

        CREATE TABLE IF NOT EXISTS guild_config (
            guild_id TEXT NOT NULL,
            key      TEXT NOT NULL,
            value    TEXT NOT NULL,
            PRIMARY KEY (guild_id, key)
        );

        CREATE TABLE IF NOT EXISTS moderation_logs (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            guild_id TEXT NOT NULL,
            channel_id TEXT NOT NULL,
            user_id TEXT NOT NULL,
            message_content TEXT NOT NULL,
            reason TEXT NOT NULL,
            severity TEXT NOT NULL,
            created_at TEXT NOT NULL DEFAULT (datetime('now'))
        );

        CREATE TABLE IF NOT EXISTS channel_prompts (
            channel_id TEXT PRIMARY KEY,
            guild_id TEXT NOT NULL,
            system_prompt TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS channel_providers (
            channel_id TEXT PRIMARY KEY,
            guild_id TEXT NOT NULL,
            provider TEXT NOT NULL
        );

        CREATE TABLE IF NOT EXISTS channel_autotranslate_settings (
            channel_id TEXT PRIMARY KEY,
            guild_id TEXT NOT NULL,
            enabled INTEGER NOT NULL DEFAULT 0,
            target_language TEXT
        );

        -- topic tags for channels (auto-generated by AI)
        CREATE TABLE IF NOT EXISTS channel_topics (
            channel_id TEXT PRIMARY KEY,
            topic TEXT
        );

        CREATE TABLE IF NOT EXISTS analytics (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            event_type  TEXT NOT NULL,
            guild_id    TEXT,
            guild_name  TEXT,
            channel_id  TEXT,
            user_id     TEXT,
            user_name   TEXT,
            provider    TEXT,
            tokens_used INTEGER,
            latency_ms  INTEGER,
            input_tokens INTEGER,
            output_tokens INTEGER,
            estimated_cost REAL,
            created_at  TEXT NOT NULL DEFAULT (datetime('now'))
        );

        CREATE TABLE IF NOT EXISTS plugins (
            name    TEXT PRIMARY KEY,
            enabled INTEGER NOT NULL DEFAULT 0
        );

        CREATE TABLE IF NOT EXISTS auto_responses (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            guild_id TEXT NOT NULL,
            keyword TEXT NOT NULL,
            response TEXT NOT NULL,
            match_type TEXT NOT NULL DEFAULT 'contains', -- 'exact' or 'contains'
            is_case_sensitive INTEGER NOT NULL DEFAULT 0,
            created_at TEXT NOT NULL DEFAULT (datetime('now'))
        );

        INSERT OR IGNORE INTO wizard_state (id) VALUES (1);
        """
    )
    # Migration for existing DB
    try:
        await db.execute("ALTER TABLE conversations ADD COLUMN guild_id TEXT")
    except:
        pass
    try:
        await db.execute("ALTER TABLE conversations ADD COLUMN category TEXT")
    except:
        pass

    # after migrations, ensure indices
    await db.executescript(
        """
        CREATE INDEX IF NOT EXISTS idx_conv_guild ON conversations(guild_id);
        """
    )

    # now create FTS and triggers once guild_id exists
    await db.executescript(
        """
        CREATE VIRTUAL TABLE IF NOT EXISTS conversations_fts USING fts5(
            content, channel_id, guild_id, content='conversations', content_rowid='id'
        );

        CREATE TRIGGER IF NOT EXISTS conversations_ai_after_insert
            AFTER INSERT ON conversations
            BEGIN
                INSERT INTO conversations_fts(rowid, content, channel_id, guild_id)
                VALUES (new.id, new.content, new.channel_id, new.guild_id);
            END;
        CREATE TRIGGER IF NOT EXISTS conversations_ai_after_update
            AFTER UPDATE ON conversations
            BEGIN
                INSERT INTO conversations_fts(conversations_fts, rowid, content, channel_id, guild_id)
                VALUES('delete', old.id, old.content, old.channel_id, old.guild_id);
                INSERT INTO conversations_fts(rowid, content, channel_id, guild_id)
                VALUES (new.id, new.content, new.channel_id, new.guild_id);
            END;
        CREATE TRIGGER IF NOT EXISTS conversations_ai_after_delete
            AFTER DELETE ON conversations
            BEGIN
                INSERT INTO conversations_fts(conversations_fts, rowid, content, channel_id, guild_id)
                VALUES('delete', old.id, old.content, old.channel_id, old.guild_id);
            END;
        """
    )

    # Backfill FTS with existing messages (migration for existing DBs)
    try:
        cursor = await db.execute("SELECT COUNT(*) FROM conversations_fts")
        row = await cursor.fetchone()
        fts_count = row[0] if row else 0
        
        if fts_count == 0:
            # FTS table is empty, backfill from conversations
            await db.execute(
                """INSERT INTO conversations_fts(rowid, content, channel_id, guild_id)
                   SELECT id, content, channel_id, guild_id FROM conversations"""
            )
            await db.commit()
    except Exception as e:
        pass  # FTS might not exist yet or other issues, silently skip

    # Analytics migration
    try:
        await db.execute("ALTER TABLE analytics ADD COLUMN input_tokens INTEGER")
    except:
        pass
    try:
        await db.execute("ALTER TABLE analytics ADD COLUMN output_tokens INTEGER")
    except:
        pass
    try:
        await db.execute("ALTER TABLE analytics ADD COLUMN estimated_cost REAL")
    except:
        pass
    try:
        await db.execute("ALTER TABLE analytics ADD COLUMN user_name TEXT")
    except:
        pass
    try:
        await db.execute("ALTER TABLE analytics ADD COLUMN guild_name TEXT")
    except:
        pass

    # Rebuild FTS5 index to ensure proper tokenization
    try:
        await db.execute('INSERT INTO conversations_fts(conversations_fts) VALUES("rebuild")')
    except Exception as e:
        pass  # FTS table might not exist yet

    await db.commit()



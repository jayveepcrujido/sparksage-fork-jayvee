async def get_total_guilds() -> int:
    """Count unique guild IDs across faqs, command_permissions, and guild_config."""
    db = await get_db()
    
    # We use a set of subqueries and UNION to get all unique guild IDs
    query = """
    SELECT COUNT(DISTINCT guild_id) as count FROM (
        SELECT guild_id FROM faqs WHERE guild_id != '1'
        UNION
        SELECT guild_id FROM command_permissions
        UNION
        SELECT guild_id FROM guild_config
    )
    """
    cursor = await db.execute(query)
    row = await cursor.fetchone()
    return row["count"] if row else 0


